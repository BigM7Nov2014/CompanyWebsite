		          <p>No doubt you are familiar with the term 'Lean'. It's become a popular terminology for change in today's corporate culture.</p>
		          <p>&nbsp;</p>
		          <p>The creation of Lean Manufacturing is generally credited to Taiichi Ohno, a Toyota production executive, in an effort to reduce waste and improve quality.</p>
		          <p>&nbsp;</p>
		          <p>And today, Lean Management techniques and principles are quite widely used throughout the Manufacturing industry. You may have heard Japanese terms like 
				  <em>Kaisen</em>, <em>Kanban</em> and <em>Muda</em>. This isn't a secret key to unlock the mysteries of Lean, they simply mean 
				  <em>Continuous Improvement</em>, <em>Pull systems</em> and <em>Non value-added work</em>. These are terms used to convey the ideas of Lean thinking.</p>
		          <p>&nbsp;</p>
		          <p>But we are often asked how a process improvement technique established specifically for manufacturing can be adapted for white collar work like Administration, Contact Centres and Document Management Centres.</p>
		          <p>&nbsp;</p>
		          <p>The truth is that 'Lean' is simply a way to reduce waste (often called Muda) using an approach that centres around creating value for your customers. The Lean approach centres around 5 categories:</p>
		          <ul class="List">
		            <li>Value</li>
		            <li>Value Stream</li>
		            <li>Flow</li>
		            <li>Pull</li>
		            <li>Perfection</li>
		          </ul>
		          <p>&nbsp;</p>
		          <p>One of the most common issues that we see with our clients, is the amount of times we hear, "I don't know why I do that" or "we've always done it this way". Sometimes, it takes someone from the outside to question the status quo.</p>
		          <p>&nbsp;</p>
		          <p>Lean Consultants spend time observing your key processes to determine if they are set up for maximum efficiency. We begin by developing simple, customer-focused process maps and work with your staff to develop a leaner, more efficent process and finally gain approval through your in-house compliance process.</p>
		          <p>&nbsp;</p>
		          <p>Some customers are concerned that their industry is heavily regulated and process changes are frowned upon, but with our approach, we take you with us every step of the way.</p>
