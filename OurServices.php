<p> <b>Consulting Service Road Map</b></p>
<p>All our consulting projects are divided into four stages based on the principle of PDCA.</P>
<p>
<b> a.)Identification: </b>
In this Stage first we identify problem area along with Top Management and then we conduct scenario analysis.
Once Scenario analysis is complete we realise Top managements expectation with strategy deployment. Based on strategy deployment we form team and detail project schedule and action plan.
</p>
<p><b> b.)Implementation:</b>
As per the schedule and action plan we implement different project simultaneously based on the availability of the team members with the help of our customized tools and methodologies.
</p>
<p><b>c.)Improvisation:</b>
As soon as project gets completed we do strategy deployment effectiveness analysis with top management and take corrective actions if necessary and repeat step 1 to step 3.
</p>
<p><b>d.)Sustain:</b>
Once the desired results are obtained we document all the learning and diffuse it to the lowest level possible in the organization for sustained results.

</p>


          
